  public init() { }

  
  public func get(countryCode: String) -> UIImage? {
    var flag:UIImage? = nil
    
    let index = countries.firstIndex(of: countryCode.lowercased())
    
    if let index = index {
      let row = index % cols
      let col = index / rows
      let x = row * width
      let y = col * height

      if let cgImage = flags?.cgImage {
        if let image = cgImage.cropping(to: CGRect(x: x, y: y, width: width, height: height)) {
          flag = UIImage(cgImage: image)
        }
      }
    }
    
    return flag
  }
  

}

